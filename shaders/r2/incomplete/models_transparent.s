function normal		(shader, t_base, t_second, t_detail)
	shader:begin	("simple",	"simple")
			: sorting	(2, true)
			: blend		(true,blend.srcalpha,blend.invsrcalpha)
			: aref 		(true,0)
			: zb 		(true,false)
			: fog		(false)
			: emissive 	(false)
	shader:sampler	("s_base")      :texture	(t_base)
end
